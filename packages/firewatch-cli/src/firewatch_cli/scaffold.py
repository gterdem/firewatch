"""Scaffold generator for FireWatch source plugins.

Called by ``firewatch new-source <name>``.  Generates a ready-to-edit plugin
package under ``<output_dir>/packages/sources/<name>/`` with:

  - pyproject.toml       — ``firewatch-sdk``-only deps + entry-point wiring
  - src/firewatch_<name>/__init__.py
  - src/firewatch_<name>/plugin.py  — SourcePlugin + Pull/Push flavor stubs
  - src/firewatch_<name>/config.py  — Pydantic config with SecretStr + env>file>default
  - src/firewatch_<name>/normalize.py — SecurityEvent mapping stub with TODOs + NB-1 note
  - src/firewatch_<name>/collector.py (pull) or listener.py (push)
  - tests/__init__.py
  - tests/test_plugin.py    — entry-point isolation test + SourcePlugin conformance
  - tests/test_normalize.py — golden-test stub the author fills in

The generated package imports ``firewatch-sdk`` ONLY — never ``firewatch-core``
or ``legacy/`` (PLUGIN_CONTRACT.md hard rule).

``type_key`` constraint: must match ``^[a-z][a-z0-9_]*$`` — must start with a lowercase letter
(leading underscore is reserved for core; see PLUGIN_CONTRACT.md and ADR-0025).
"""
from __future__ import annotations

import re
from pathlib import Path
from string import Template


# ---------------------------------------------------------------------------
# Name validation
# ---------------------------------------------------------------------------

_TYPE_KEY_RE = re.compile(r"^[a-z][a-z0-9_]*$")


def validate_name(name: str) -> None:
    """Raise ``ValueError`` if *name* is not a valid source type_key.

    PLUGIN_CONTRACT.md: ``type_key`` must match ``^[a-z][a-z0-9_]*$``
    (must start with a lowercase letter; leading underscore is reserved for core).
    """
    if not name:
        raise ValueError("Source name must not be empty.")
    if not _TYPE_KEY_RE.match(name):
        raise ValueError(
            f"Source name {name!r} is invalid. "
            "It must match ^[a-z][a-z0-9_]*$ (must start with a lowercase letter; "
            "digits and underscores may follow). "
            "Example: 'azure_waf', 'syslog2', 'mywidget'."
        )


# ---------------------------------------------------------------------------
# Template helpers
# ---------------------------------------------------------------------------

def _camel(name: str) -> str:
    """Convert snake_case name to CamelCase for use as a class name.

    Examples:
      'mywidget'   -> 'Mywidget'
      'my_widget'  -> 'MyWidget'
      'azure_waf2' -> 'AzureWaf2'
    """
    return "".join(part.capitalize() for part in name.split("_"))


def _env_prefix(name: str) -> str:
    """Return the env-var prefix for the source: FIREWATCH_<NAME>_."""
    return f"FIREWATCH_{name.upper()}_"


