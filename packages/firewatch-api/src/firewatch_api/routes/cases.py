"""Case File routes (ADR-0053 D4 / issue #534 + #535).

Routes
------
POST /cases
    Create a new case file. Returns 201 + {"id": <int>}.

GET /cases
    Cursor-paginated list of cases (ADR-0029 envelope).

GET /cases/{case_id}
    Full case_file record.  Returns 404 when not found.

PATCH /cases/{case_id}/disposition
    Set the case disposition (true-positive / false-positive / benign / open).
    Returns 200 on success; 404 when case not found; 422 on invalid value.

POST /cases/{case_id}/notes
    Add a markdown note to a case.  Returns 201 + {"id": <int>}.
    author defaults to 'local operator' (ADR-0053 D3 auth-aware seam).

GET /cases/{case_id}/notes
    Return all notes for a case in chronological order.

GET /cases/{case_id}/timeline
    Assemble event + analysis references at read time (ADR-0041 / ADR-0053 D2).
    Returns 404 when case not found.

POST /cases/{case_id}/events
    Link a security_event or ai_analysis reference to a case (EARS-2).
    Returns 201 + {"id": <int>}.

POST /cases/{case_id}/summary  (issue #535 B1-polish, ADR-0053 D2)
    Generate a local-LLM draft summary for the case, reusing the ML-7
    narration path (ADR-0043 / EARS-1).  Persists the draft as a case_note
    with ai_drafted=1 (ADR-0035 / EARS-2).  Degrades to rule-only summary
    when the LLM is unavailable (EARS-5).  Suggest-only — no auto-disposition
    (EARS-6 / ADR-0015).
    Returns 201 + {"note_id", "narrative", "provenance", "collected_fields", "ai_status"}.

Route class: B (writes) / C (reads) — ADR-0026 gating applies when the API
is exposed beyond loopback (deferred to MP.3 / ADR-0026 Decisions 2-3 / issue #548).

Security
--------
- body_md is operator text that may embed attacker-controlled content (OWASP LLM01);
  it must be rendered as sanitized markdown in the UI — never raw HTML (ADR-0029 D3).
- All parameterised queries in the adapter; no interpolation.
- 404 errors do NOT echo attacker-controlled record content (OWASP API4:2023).
- limit clamped to 200 (prevents large inadvertent reads).
"""
from __future__ import annotations

import logging
from typing import Any, Literal

from fastapi import APIRouter, HTTPException, Query, Request
from pydantic import BaseModel, Field

logger = logging.getLogger("firewatch.api.cases")

router = APIRouter(tags=["cases"])

_DEFAULT_LIMIT = 50
_MAX_LIMIT = 200

# Valid disposition values (mirrors caps.py; kept here for Pydantic Literal check).
_DispositionLiteral = Literal["true-positive", "false-positive", "benign", "open"]


# ---------------------------------------------------------------------------
# Request bodies
# ---------------------------------------------------------------------------


class _CreateCaseBody(BaseModel):
    """Request body for POST /cases."""

    title: str = Field(..., min_length=1, max_length=500, description="Case title")
    subject: str = Field(
        ..., min_length=1, max_length=500,
        description="Entity under investigation (e.g. source IP, hostname)",
    )
    disposition: _DispositionLiteral = Field(
        default="open", description="Initial disposition"
    )


class _AddNoteBody(BaseModel):
    """Request body for POST /cases/{case_id}/notes.

    author defaults to 'local operator' (ADR-0053 D3 auth-aware seam).
    Post-ADR-0026, the API layer can override author from the authenticated principal.
    """

    body_md: str = Field(
        ..., min_length=1,
        description="Markdown note body (max 32 KiB). Rendered as sanitized markdown in the UI.",
    )
    author: str = Field(
        default="local operator",
        min_length=1,
        max_length=500,
        description="Note author identity. Defaults to 'local operator'.",
    )
    ai_drafted: bool = Field(
        default=False, description="True when the body was generated by the local LLM (ADR-0035)."
    )


class _SetDispositionBody(BaseModel):
    """Request body for PATCH /cases/{case_id}/disposition."""

    disposition: _DispositionLiteral = Field(..., description="New disposition value")


class _LinkEventBody(BaseModel):
    """Request body for POST /cases/{case_id}/events."""

    ref_kind: str = Field(
        ..., min_length=1, max_length=100,
        description="Kind of reference: 'security_event' or 'ai_analysis'.",
    )
    ref_id: str = Field(
        ..., min_length=1, max_length=200,
        description="Stringified ID in the referenced table.",
    )


# ---------------------------------------------------------------------------
# Dependency helper
# ---------------------------------------------------------------------------


def _get_case_store(request: Request) -> Any | None:
    """Resolve the SqliteCaseStore from app state.

    Returns None when the store is not yet wired (degrades to 503).
    """
    return getattr(request.app.state, "case_store", None)


def _require_case_store(request: Request) -> Any:
    store = _get_case_store(request)
    if store is None:
        raise HTTPException(
            status_code=503,
            detail="Case store not available — service starting up.",
        )
    return store


# ---------------------------------------------------------------------------
# POST /cases
# ---------------------------------------------------------------------------


@router.post("/cases", status_code=201, response_model=None,
             summary="Create a new case file")
async def create_case(request: Request, body: _CreateCaseBody) -> dict[str, Any]:
    """Create a new case file (EARS-1).

    Returns ``{"id": <int>}`` on success (201).
    Returns 503 when the case store is not wired.
    """
    store = _require_case_store(request)
    try:
        case_id = await store.create_case(
            title=body.title,
            subject=body.subject,
            disposition=body.disposition,
        )
    except ValueError as exc:
        raise HTTPException(status_code=422, detail=str(exc))
    except Exception as exc:
        logger.error("POST /cases: create_case failed: %s", exc, exc_info=True)
        raise HTTPException(status_code=503, detail="Case store write failed.") from exc
    return {"id": case_id}


# ---------------------------------------------------------------------------
# GET /cases
# ---------------------------------------------------------------------------


@router.get("/cases", response_model=None, summary="Paginated list of cases")
async def list_cases(
    request: Request,
    limit: int = Query(default=_DEFAULT_LIMIT, ge=1, le=_MAX_LIMIT),
    cursor: str | None = Query(default=None),
    subject: str | None = Query(default=None, description=(
        "Optional equality filter on the case subject field (e.g. source IP). "
        "When provided, only cases whose subject exactly matches this value are "
        "returned, newest first. When omitted, all cases are returned (issue #757)."
    )),
) -> dict[str, Any]:
    """Return a cursor-paginated list of case files (ADR-0029 envelope).

    Newest cases first. Response: ``{"items": [...], "next_cursor": str|null, "has_more": bool}``.

    When ``subject`` is provided, the result is filtered to cases whose
    ``subject`` field exactly equals that value (parameterized SQL — no
    injection risk).  The frontend uses this for find-or-create dedup (issue #757).
    """
    store = _require_case_store(request)
    try:
        page = await store.list_cases(limit=limit, cursor=cursor, subject=subject)
    except Exception as exc:
        logger.error("GET /cases: list_cases failed: %s", exc, exc_info=True)
        raise HTTPException(status_code=503, detail="Case store read failed.") from exc
    return page


# ---------------------------------------------------------------------------
# GET /cases/{case_id}
# ---------------------------------------------------------------------------


@router.get("/cases/{case_id}", response_model=None, summary="Get a case file by ID")
async def get_case(request: Request, case_id: int) -> dict[str, Any]:
    """Return the full case_file record for *case_id*.

    Returns 404 when the case does not exist.
    Returns 503 when the store is not wired.
    """
    store = _require_case_store(request)
    try:
        case = await store.get_case(case_id)
    except Exception as exc:
        logger.error("GET /cases/%d: get_case failed: %s", case_id, exc, exc_info=True)
        raise HTTPException(status_code=503, detail="Case store read failed.") from exc
    if case is None:
        raise HTTPException(status_code=404, detail=f"Case {case_id} not found.")
    return case


# ---------------------------------------------------------------------------
# PATCH /cases/{case_id}/disposition
# ---------------------------------------------------------------------------


@router.patch(
    "/cases/{case_id}/disposition",
    response_model=None,
    summary="Set the case disposition",
)
async def set_disposition(
    request: Request, case_id: int, body: _SetDispositionBody
) -> dict[str, Any]:
    """Update the disposition of a case (EARS-5).

    Valid values: ``true-positive``, ``false-positive``, ``benign``, ``open``.
    Returns ``{"ok": true}`` on success.
    Returns 404 when the case does not exist; 422 on invalid disposition.
    """
    store = _require_case_store(request)
    try:
        await store.set_disposition(case_id=case_id, disposition=body.disposition)
    except LookupError:
        raise HTTPException(status_code=404, detail=f"Case {case_id} not found.")
    except ValueError as exc:
        raise HTTPException(status_code=422, detail=str(exc))
    except Exception as exc:
        logger.error(
            "PATCH /cases/%d/disposition: set_disposition failed: %s",
            case_id, exc, exc_info=True,
        )
        raise HTTPException(status_code=503, detail="Case store write failed.") from exc
    return {"ok": True}


