# POST /cases/{case_id}/notes
# ---------------------------------------------------------------------------


@router.post(
    "/cases/{case_id}/notes",
    status_code=201,
    response_model=None,
    summary="Add a markdown note to a case",
)
async def add_note(
    request: Request, case_id: int, body: _AddNoteBody
) -> dict[str, Any]:
    """Append a markdown note to a case (EARS-3 / EARS-4).

    ``author`` defaults to ``'local operator'`` (ADR-0053 D3 auth-aware seam).
    Returns ``{"id": <int>}`` on success (201).
    Returns 404 when the case does not exist; 422 when body exceeds cap.

    Security: ``body_md`` may embed attacker-controlled content; it MUST be
    rendered as sanitized markdown in the UI — never raw HTML (ADR-0029 D3).
    """
    store = _require_case_store(request)
    try:
        note_id = await store.add_note(
            case_id=case_id,
            body_md=body.body_md,
            author=body.author,
            ai_drafted=body.ai_drafted,
        )
    except LookupError:
        raise HTTPException(status_code=404, detail=f"Case {case_id} not found.")
    except ValueError as exc:
        raise HTTPException(status_code=422, detail=str(exc))
    except Exception as exc:
        logger.error(
            "POST /cases/%d/notes: add_note failed: %s", case_id, exc, exc_info=True
        )
        raise HTTPException(status_code=503, detail="Case store write failed.") from exc
    return {"id": note_id}


