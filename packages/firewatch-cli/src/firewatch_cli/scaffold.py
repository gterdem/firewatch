def _render(tmpl: Template, **kwargs: str) -> str:
    """Render a Template with the given keyword arguments."""
    return tmpl.substitute(**kwargs)


def scaffold(
    name: str,
    *,
    flavor: str,
    output_dir: Path,
) -> Path:
    """Generate a new source plugin package skeleton.

    Parameters
    ----------
    name:
        The source type key (must match ``^[a-z][a-z0-9_]*$``;
        must start with a lowercase letter).
    flavor:
        Either ``"pull"`` or ``"push"``.
    output_dir:
        Root directory under which ``packages/sources/<name>/`` is created.

    Returns
    -------
    Path
        The path of the generated package root (``<output_dir>/packages/sources/<name>/``).

    Raises
    ------
    ValueError
        If *name* is invalid or *flavor* is not ``"pull"``/``"push"``.
    FileExistsError
        If the target directory already exists (no silent overwrite).
    """
    validate_name(name)

    if flavor not in ("pull", "push"):
        raise ValueError(
            f"flavor must be 'pull' or 'push', got {flavor!r}."
        )

    pkg_root = output_dir / "packages" / "sources" / name
    if pkg_root.exists():
        raise FileExistsError(
            f"Package directory already exists: {pkg_root}\n"
            f"Refusing to overwrite '{name}'. "
            "Remove the directory manually if you want to regenerate."
        )

    class_name = _camel(name)
    title = name.replace("_", " ").title()
    env_prefix = _env_prefix(name)

    ctx: dict[str, str] = {
        "name": name,
        "class_name": class_name,
        "title": title,
        "env_prefix": env_prefix,
    }

    src_dir = pkg_root / "src" / f"firewatch_{name}"
    tests_dir = pkg_root / "tests"

    src_dir.mkdir(parents=True)
    tests_dir.mkdir(parents=True)

    # ── pyproject.toml ────────────────────────────────────────────────────────
    (pkg_root / "pyproject.toml").write_text(_render(_PYPROJECT_TMPL, **ctx), encoding="utf-8")

    # ── src/firewatch_<name>/__init__.py ──────────────────────────────────────
    (src_dir / "__init__.py").write_text("", encoding="utf-8")

    # ── src/firewatch_<name>/plugin.py ────────────────────────────────────────
    plugin_tmpl = _PLUGIN_PULL_TMPL if flavor == "pull" else _PLUGIN_PUSH_TMPL
    (src_dir / "plugin.py").write_text(_render(plugin_tmpl, **ctx), encoding="utf-8")

    # ── src/firewatch_<name>/config.py ────────────────────────────────────────
    (src_dir / "config.py").write_text(_render(_CONFIG_TMPL, **ctx), encoding="utf-8")

    # ── src/firewatch_<name>/normalize.py ─────────────────────────────────────
    (src_dir / "normalize.py").write_text(_render(_NORMALIZE_TMPL, **ctx), encoding="utf-8")

    # ── src/firewatch_<name>/collector.py or listener.py ─────────────────────
    if flavor == "pull":
        (src_dir / "collector.py").write_text(_render(_COLLECTOR_TMPL, **ctx), encoding="utf-8")
    else:
        (src_dir / "listener.py").write_text(_render(_LISTENER_TMPL, **ctx), encoding="utf-8")

    # ── tests/__init__.py ─────────────────────────────────────────────────────
    (tests_dir / "__init__.py").write_text("", encoding="utf-8")

    # ── tests/test_plugin.py ─────────────────────────────────────────────────
    (tests_dir / "test_plugin.py").write_text(_render(_TEST_PLUGIN_TMPL, **ctx), encoding="utf-8")

    # ── tests/test_normalize.py ──────────────────────────────────────────────
    (tests_dir / "test_normalize.py").write_text(_render(_TEST_NORMALIZE_TMPL, **ctx), encoding="utf-8")

    return pkg_root
